import java.util.*;
public class Binary1Operators{
    public static void main(String args[]){
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~(1));
        System.out.println(5<<2);
        System.out.println(6>>1);
    }
}